package pack02.variabletype;

public class Ex06_Double {
	public static void main(String[] args) {
		//정수형 데이터 타입을 이전 시간까지 살펴 봤음.(int short long byte char)
		//int pie = 3.14;(x) 3.14는 소숫점을 가지고 있음.
		//float(↑) , double(↑)
		float pie_f = 3.14F;//long과 마찬가지로 뒤에 데이터타입의 구분이 필요함.
		double pie_d = 3.14;
		System.out.println(pie_f);
		System.out.println(pie_d);
	}
}
