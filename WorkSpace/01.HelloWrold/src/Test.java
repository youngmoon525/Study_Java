
// 주석 : 설명을 써놓을 때 사용함. "//"
// { } : 블럭킹. 중괄호가 열리고 닫히는 것. 반드시 한 쌍임.
// { 중괄호가 열림 : 프로그램의 시작. 
// } 중괄호가 닫힘 : 프로그램의 끝.
// main 메소드 : 자바 프로그램의 첫부분을 의미함.
// ※ 중괄호가 무엇을 의미하는지 추상적으로 이해 , 규칙적으로 main이라는 메소드를 만들고 그 안에서 작업.
public class Test {
	public static void main(String[] args) {
		// 항상 우리는 여기에서 작업을 합니다.
		System.out.println("프로그램이 실행이 되었는지 보기 위해서 출력을 한다.");
	}
}
